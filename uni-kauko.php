                 <div class="tab-pane fade" id="advantages">
                   <p>   В университете налажены постоянные встречи с предпринимателями из Литвы и других стран. Гостями университета становятся главы передовых бизнес-организаций, которые своими опытом и знаниями оказывают непосредственной влияние на развитие студентов.</p>

                   <p>   Университет в тесном сотрудничестве с социальными партнерами в области прикладных исследований и творческой деятельности организовывает научно-практические конференци, отличительной чертой которых являются темы, отражающие реализацию «треугольника знаний» (прикладные исследования, интеграции исследований, и связь с бизнесом).</p>

                   <p>   Все студенты имеют полный доступ к компьютерным центрам расположенных на всех факультетах, также функционирует центр Самообучения, помогающий развиваться самостоятельно. Во многих зонах интернета функционирует беспроводной интернет.</p>

                   <p>   Студенты имеют возможность пользоваться современной университетской библиотекой, насчитывающей более чем 160 000 томов и печатных изданий. </p>
                   <p>   Библиотека университета прикладных наук является членом библиотечной сети ВУЗов (LABT) и системы Интегративных Информационных Библиотек Литвы (LIBIS). Библиотека максимально автоматизирована и поиск нужной информации занимает минимум времени.</p>

                   <p>  <strong> ERASMUS+</strong> - Программа мобильности предусматривает обучение студентов в высших учебных заведениях других стран длительностью от 3 месяцев до одного года. Цель этой программы – совершенствовать образование студентов и развивать опыт обучения за рубежом.</p>
