                   <p><strong>Каунасский Университет Прикладных Наук</strong> является государственной организацией, которая предоставляет высшее образование в области технологий, социальных наук, биомедицины, гуманитарных и искусства. Это одно из самых больших высших учебных заведений не только в Литве, но и во всем Балтийском регионе.</p>

                   <p>   История Каунасского Университета Прикладных Наук берет начало, с открытия специализированных школ во времена 2 мировой войны и послевоенных технических школ. Нынешний факультет Технологий вырос из Целлюлозно - бумажной Промышленной школы, основанной в Каунасе в 1954 году. Также факультет Экономики и Закона изначально являлся Технической школой статистики, основанной в Каунасе в 1954 году. История других факультетов относится еще к более раним временам.</p>

                   <p>   Нынешний Факультет Здравоохранения создан на основе краткосрочных курсов для акушерок и двух летних курсов сестер милосердия открытых профессором Pranas Mažylis и Литовским Красным Крестом в 1920 году. В 1941 году данные курсы стали Каунасской Школой Ухода. Факультет Искусства создан на основе курсов рисования, проходивших в Школе Искусства, открытой по инициативе живописца Justinas Vienožinskis в 1922 году. В 1939 году школа стала Высшим Учебным Заведением.</p>

                   <p>   Сегодня Каунасский Университет Прикладных Наук одно из крупнейших высших учебных заведений в Литве, с 7 600 студентами, 1 000 сотрудников и 20 000 выпускников, которые успешно работают в литовских и зарубежных компаниях. В 2011 и 2013 годах университет занимает 1 место в рейтинге лучших литовских колледжей.</p>
