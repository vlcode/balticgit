                   <p> Как правило, владелец комнаты или квартиры будет просить студента заплатить в конце месяца. Студент и владелец могут также решить между собой на день оплаты. Аренда комнаты в доме студентов, как правило, выплачивается в определенный день, что будет сказано студенту по прибытии.</p>

