                    <p>Международный университет LCC (в прошлом - Высшая школа литовского христианского фонда) - это образовательное учреждение гуманитарных наук, находящееся в городе Клайпеде (Литва), получившее признание как в Литве, так и за рубежом. Основанный в 1991 году совместными усилиями литовского, канадского и американского фондов, среди местных высших учебных заведений LCC выделяется своим уникальным, ориентированным на будущее подходом к образованию и учебной средой, основанной на свободном диалоге. Образование LCC базируется на преподавании широкого спектра дисциплин в контексте христианского студенческо-преподавательского состава, таким образом, вынося образование за рамки аудитории. Поскольку частью концепции LCC является подготовка студентов к активному участию в общественной жизни своего города, особо подчёркивается роль каждого в построении сообщества посредством активного участия в общественных проектах и практических занятиях.</p>

