               <h2>Об университете</h2>

              <p>Клайпедский колледж является третьим по величине колледжеv в Литве с весомой академической репутацией. Первоначальная цель университетаэто обеспечение высокого качества академических учебных и научно-исследовательских учреждений. Все учебные программы ориентированы на реальные знания предъявляемые к выбранной профессии. Исследования также являются практически ориентированными, студенты имеют возможность успешно применять свои знания в реальной рабочей среде.</p>

            <p>  Клайпедский колледж состоит из трех факультетов: Общественные науки, технологии и наука о здоровье. Также университет проводит множество исследований в сфере технологий, биомедицины, физических, социальных и гуманитарных наук, а так же в области искусства.</p>


              <h3>  Факультет социальных наук</h3>
            <p>  Предлагает учебные программы в области бухгалтерского учета, финансов, администрации и туризма; логистики, торговли, спорта, развлечений и управления бизнесом, а также педагогики начальной школы, английского языка и социальной педагогики.</p>
            <p>  На данный момент на этом факультете обучается 1692 студентов.</p>

              <h3>   Факультет Технологии</h3>
            <p>  Предоставляет программы в различных технических профессий, таких как: строительство, машиностроение, геодезия, ландшафтный дизайн, информатика, технологии производства продуктов питания и автомобильные технологии.</p>
            <p>  На данный момент на этом факультете обучается 1322 студентов.</p>

              <h3>   Факультет науки здравоохранения</h3>
            <p>  Студенты могут выбрать программы сестринского дела, физиотерапии, гигиена полости рта, одонтологической помощи, а также социальную работу для своих исследований.</p>
            <p>  На данный момент на этом факультете обучается 925 студентов.</p>
