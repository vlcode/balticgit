                 <p>Выпускники Вентспилской высшей школы успешно:</p>
                 <ul>
                  <li> переводят тексты разной степени сложности во многих сферах и отраслях;</li>
                  <li> редактируют свои и чужие переводы;</li>
                  <li> используют современные компьютерные технологии;</li>
                  <li> самостоятельно ведут научную деятельность в сфере переводоведения;</li>
                  <li> решают разнообразные профессиональные проблемы;</li>
                  <li> работают на международном рынке труда.   </li>
