              <p>  Благодаря учебе в ВВШ ты получишь возможность:</p>
                <ul>
                  <li>углубить знания иностранных рабочих языков (английского, немецкого или французского);</li>
                <li>  довести до совершенства навыки перевода на свой родной язык – русский;</li>
                  <li>по желанию, освоить еще один язык ЕС – латышский;</li>
                  <li>ориентироваться в теоретических и практических вопросах переводоведения, межкультурной коммуникации и языкознания;</li>
                <li>  стать компетентным специалистом-переводчиком, обладающим знаниями в сферах коммерческой деятельности, маркетинга, делопроизводства, юриспруденции;</li>
                <li>  овладеть самыми разными техниками перевода – от письменного до синхронного;</li>
                <li>  получить углубленные знания об англоязычных странах и погрузиться в иностранную среду в рамках программы по обмену студентами ERASMUS;</li>
                <li>  отшлифовать полученные в вузе навыки во время 4-месячной практики на одном из предприятий, действующем на рынке ЕС;</li>
                <li>  разработать и защитить бакалаврскую работу на одну из актуальных тем в сфере переводоведения.  </li>
                </ul>

            <p>    Все студенты ВВШ могут принимать участие в программах по обмену студентами ERASMUS и в течение семестра учиться в одном из европейских институтов. Тебя ждет незабываемый семестр в Германии, Франции, Норвегии, Турции или другой стране!</p>

            <p>    Мы предлагаем возможность участвовать в разнообразных международных проектах.</p>

            <p>    Твоим будущим местом работы могут стать европейские переводческие бюро, международные организации, частные и государственные предприятия.</p>

            <p>    Академический персонал, осуществляющий программу «Перевод с/на английский – русский – немецкий/французский», состоит из профессиональных педагогов, переводчиков и специалистов в разных сферах, международных экспертов, председателей научных советов и комитетов, членов Европейского общества ученых-переводчиков. Практические занятия по письменному и устному переводу ведутся на русском языке, но большой пласт лекций преподается и на английском, что позволяет студентам получать разносторонние теоретические знаниями и овладевать терминологией в разных научных, экономических и социально-политических областях.</p>

            <p>    Здесь ты полностью освоишь фонетику, стилистику и грамматику рабочих языков, получишь практикуустного и письменного перевода, узнаешь о международном предпринимательстве, разберешься в теоретических и практических вопросах сопоставительного языкознания, переводоведения и терминологии, научишься разрабатывать исследовательскую работу. Курсы по выбору также предлагают углубить знания в культурологии, истории, этике, компьютерных технологиях, социологии, литературе, естествознании и др.</p>


