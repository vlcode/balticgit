               <h2>Об университете</h2>
               Вильнюсский бизнес колледж (VVK) – это инновационное, признанное на международном уровне высшее учебное заведение, предоставляющее возможность получить качественное европейское высшее образование (степень бакалавра) в области социальных, гуманитарных и физических наук.

               В колледже преподают квалифицированные специалисты из Литвы и зарубежных стран, многие из которых являются практиками. Все преподаватели имеют как минимум степень магистра, некоторые из них – ученые, имеющие степень доктора наук по различным специальностям, а также ученое звание доцента или профессора.

