               <h2>Об университете</h2>
              <p>  Резекненская Академия Технологий создана на базе Латвийского университета и Рижско- технического университета. </p>
              <p>  В 1992 году была идея создания независимого университета. В том же году, 28 августа Конгресс принял резолюцию, призывающую к приоритетным направлениям, а также все общественного порядка был поднят в управлении системой образования.</p>
              <p>  В связи с этим, в 1993 году РА возникло как самостоятельное учебное заведение. РА с 1999 года имеет международную аккредитацию и стал важным культурным, образовательным центром науки.</p>
              <p>  В 1995 году RHEI была интегрирована в Резекненский государственный педагогический институт.</p>
              <p>  Со временем расширилась на четыре факультета: гуманитарных, юридических наук, экономики, техники и обучение.</p>
              <p>  С 1993 - 2015 года название вуза было Резекненская высшая школа. По причине улучшения качества образования и расширения профиля Резекненская высшая школа получила название Резекненской Академии Технологий. </p>

