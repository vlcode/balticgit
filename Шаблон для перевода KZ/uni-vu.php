               <h2 class="hvr-icon-down" data-toggle="collapse" data-target="#hide-me3">Преимущества обучения</h2>
               <div id="hide-me3" class="collapse">
              <p>   На базе университета действуют Центры открытого доступа, оснащенные сверхсовременным лабораторным оборудованием, и научно – исследовательские возможности которых доступны не только для исследователей, студентов и ученых Вильнюсского университета, но и для исследователей и студентов из других научных учреждений и предприятий. </p>
                <h3> Центры открытого доступа (OAC) в Вильнюсском университете: </h3>
                 <ul>
                <li>   Vilnius University Joint Life Sciences Center </li>
                <li>   Vilnius University Information Technology Research Center </li>
                <li>   Vilnius University Lasers Research Center facility “Naglis” </li>
                <li>   Vilnius University Physical Sciences and Technologies Research Center </li>
                 </ul>
              <p>   Университет успешно реализует проекты поддержки студентов в научной, исследовательской, предпринимательской, инновационных областях. </p>
                 <h3>Инновационное продвижение бизнеса (INOVEKS)</h3>
              <p>   Целью проекта было создание возможностей для студентов, докторантов и молодых исследователей для генерации бизнес-идей, внедрение новых технологии с высокой добавленной стоимостью стартапов, обеспечение советами в создании бизнес модели и получение льготных условий для развития бизнеса.</p>
                <h3> Открытый доступ к науке и исследованиям (MITAP) </h3>
            <p>     Целью проекта было способствование передаче технологий в центрах открытого доступа с целью укрепления международной конкурентоспособности литовских исследователей в Европейское исследовательское пространство. </p>
                <h3> Деловое сотрудничество университетов </h3>
              <p>   Проект был направлен на укрепление сотрудничества между высшими учебными заведениями и предприятиями путем выявления наиболее важных предпосылок, которые делают университетское деловое сотрудничество взаимно значимым и ценным. Это было достигнуто путем анализа уже реализованных подобных проектов, для понимания предпосылок для успешного, конструктивного и ценного сотрудничество. Проект охватывал 7 стран - Латвия, Литва, Финляндия, Швеция, Норвегия, Эстония и Дания. </p>
              <p>  <strong> ERASMUS+</strong> - Программа мобильности предусматривает обучение студентов в высших учебных заведениях других стран длительностью от 3 месяцев до одного года. Цель этой программы – совершенствовать образование студентов и развивать опыт обучения за рубежом.</p>
