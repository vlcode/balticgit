               <h2 class="hvr-icon-down" data-toggle="collapse" data-target="#hide-me2">Проживание</h2>

               <div id="hide-me2" class="collapse">
                <p>  Общежитие Вильнюсского университета доступно для студентов только в течение академического года (с 1 Сентября по 30 Июня).</p>
                <p>  Университет может предложить студентам 3 общежития, находящихся на улицах Saulėtekio, Olandų и Didlaukio. Все общежития находятся рядом с отделениями университета.</p>

                <h3>  Общежитие на улице Saulėtekio.</h3>
                <p>  Расположено не далеко от факультетов Экономики, Физики, Коммуникации и законодательства. Студенты живут в двухместных и трехместных номерах. На каждом этаже есть общая кухня, которую делят 23 человека. Ванная и туалет также общие, делятся они между 5 студентами. Общежитие предоставляет студентам все необходимые спальные принадлежности, в здании есть прачечная, а также беспроводной интернет.</p>

                <h3>  Общежитие на улице Olandų.</h3>
              <p>    Расположено рядом со старым кампусом университета, где находятся факультет Филологии, Философии и Истории. Студенты проживают в двухместных и трехместных номерах. В общежитие есть комната отдыха с доступом в интернет. На каждом этаже есть общая кухня, которую делят 18 человека. Ванная и туалет также общие, делятся они между 5 студентами. Общежитие предоставляет студентам все необходимые спальные принадлежности, в здании есть прачечная, а также беспроводной интернет.</p>

                <h3>  Общежитие на улице Didlaukio.</h3>
                <p>  Находится на одинаковом расстоянии ко все факультетам. Студенты живут в двухместных и трехместных номерах. На каждом этаже есть общая кухня, которую делят 23 человека. Ванная и туалет также общие, делятся они между 5 студентами. Общежитие предоставляет студентам все необходимые спальные принадлежности, в здании есть прачечная и доступ к интернету.</p>

