               <h2>Об университете</h2>
                <p>Университет VIKO был основан в 2000 году и на данный момент здесь обучается более 7100 студентов (2013 год), по 42 учебным программа на 7 факультетах: Электроника и Информатика, Экономика, Управление Бизнесом, Здравоохранение, Педагогика, Искусство и креативные технологии и Агротехника. Колледж появился на сильных корнях трех лучших высших школ в Вильнюсе: Вильнюсская Высшая школа электроники, Вильнюсская Высшая школа экономики и Вильнюсская Высшая школа бизнеса, которые были объединены в один объект. После того, как были созданы учебные программы и одобрены внешними оценщиками как на национальном, так и на международном уровне, вышеупомянутые высшие учебные заведения объединены в одно целое и получил статус колледжа высшего образования.</p>

