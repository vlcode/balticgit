              <p>  Формы обучения</p>
              <p>  Очная форма обучения – обучение ведется с отрывом от работы, в отличии от дистанционной и заочной формы. Структура обучения построена на личном контакте студента с преподавателем, что помогает качественному усвоению материала и получению более глубоких знаний. Студенты очной формы обучения обязаны посещать лекции, семинары и проходить практику.</p>


              <p>  Заочная форма обучения – сочетает в себе самообучение и очное обучение. Большую часть предметов студенты осваивают самостоятельно. Основным отличием заочной формы обучение является ее продолжительность и возможность продолжения рабочей деятельности. Студенты заочной формы обучения учатся в два периода. Первый период – это вводные лекции, на которых преподаватели дают список литературы, темы рефератов, контрольные задания и т.д. Второй период – это экзаменационные сессии.</p>

              <p>  Дистанционная форма обучения – обучение проходит на расстоянии, посредством интернет – технологий и других средств интерактивной коммуникации. Основным отличием дистанционной формы обучения является составление индивидуального учебного плана, самим студентом и согласованным с преподавателем. Студент может получить консультации у своего преподавателя по интересующим его вопросам с помощью интернета, телефона и т.д. Диплом об окончании дистанционной формы обучения ничем не отличается от обычного.</p>

