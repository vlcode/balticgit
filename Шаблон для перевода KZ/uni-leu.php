               <h2>Об университете</h2>
              <p> В 1935 году Правительство Литовской Республики основало первое высшее учебное заведение для подготовки учителей - Педагогический институт с двухгодичным образованием в Клайпеде. Миссия института состояла в подготовке учителей и развитии педагогических наук во всей Литве. В марте 1939 года, после оккупации нацисткой Германией Клайпедского края, институт был переведен в Паневежис, а осенью того же года в Вильнюс, в возвращенную Литве столицу. Педагогический институт был переименован в Вильнюсский педагогический институт.</p>

            <p>  После восстановления независимости Литвы 11 марта 1990 года, указом Верховного Совета Литовской республики институт получил статус университета. В октябре 2011 года Вильнюсский педагогический университет получил новое название - Литовский эдукологический университет.</p>

            <p>  На сегодняшний день Литовский эдукологический университет принимает активное участие в реформах образования и сотрудничает с различными учреждениями, развивающими личность, включая учреждения дошкольного возраста, среднего и высшего образования, а также учреждениями профессионального образования и образования взрослых. Университет оказывает непосредственное влияние на другие вузы Литвы, так как развитие тех, кто становится частью высшего образования, зависит от того, какие учителя их обучали в школе, а именно - от выпускников ЛЭУ.</p>
