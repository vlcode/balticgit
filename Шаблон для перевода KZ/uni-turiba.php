<p>Молодежный туристический хостел способен расселить в своих 148 благоустроенных номерах свыше 550 человек. На каждом этаже расположены общие душевые, туалеты и две кухни. Также в общежитии есть компьютерная комната с интернетом и комната отдыха<br>
