               <h2>Об университете</h2>

               <p>Основанный в 1993 году, университет Turiba является одним из ведущих вузов в Латвии. За 22 года существования в университете появились свои устоявшиеся традиции и определенное видение будущего.</p>

<p>В университете Turiba знания идут рука об руку с практическими навыками, что способствует подготовке квалифицированных специалистов, высоко ценимых работодателями не только в Латвии, но и во всем мире. Университет добился признание большого количества организаций и занял место среди самых признаваемых университетов.</p>

