               <h2>Об университете</h2>
               <p>RTTEMA является концептуально и технически современным государственным высшим учебным заведением, основанным в 1994 году. На момент основания вуза в нем обучалось 1 273 студента и в настоящий момент RTTEMA является одним из лидеров среди высших учебных заведений Латвии по количеству обучаемых студентов.</p>
               <p>Не смотря на свой юный возраст, университет унаследовал лучшие традиции вузов и учителей 1 946 годов. RTTEMA был основан благодаря слиянию двух университетов: Рижского Педагогического Института и Имантского Педагогического Института.</p>
               <p>Цель университета предоставить каждому студенту возможность приобрести профессиональное и высшее академическое образование в области педагогики, психологии, бизнеса и менеджмента, а также мотивацию к самосовершенствованию.</p>
               <p>RTTEMA готовит учителей, которые в состоянии подготовить учеников к жизни и работе в информационном обществе. Концептуальные краеугольные камни процесса обучения RTTEMA - интеллект, компетентность и мастерство. Каждая учебная программа позволяет нашим студентам развивать необходимые профессиональные навыки.</p>
