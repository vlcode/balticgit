               <h2>Об университете</h2>
            <p>   Литовский университет спорта - уникальное, по своему направлению, высшее учебное заведение, предлагающее образовательные программы в области физической культуры, спорта и реабилитции.</p>

            <p>   Основанием университета считаются высшие курсы по физической культуре, учрежденные в Каунасе в 1934 г., выпускники которых получали диплом высшего образования. Это давало выпускникам право преподавать спортивно-физкультурные предметы в гимназиях (школах). В 1945 году, уже как самостоятельное высшее учебное заведение, был основан Литовский государственный институт физической культуры. В 1999 году институт был переименован в Литовскую академию физической культуры, а с ноября 2012 года он носит название Литовский университет спорта.</p>

              <p>  Университет является крупнейшим в Литве центром физического образования и спортивной науки, поддерживающий спортивные ценности и традиции страны. С момента основания в университете было подготовлено свыше 14 тысяч учителей физической культуры, тренеров по многим видам спорта, кинезитерапевтов, менеджеров по спорту и туризму, квалифицированных специалистов других профилей. Среди выпускников университета – много видных учёных, всемирно известных тренеров, выдающихся спортсменов, известных общественных деятелей. Ряд студентов и выпускников стали олимпийскими чемпионами, чемпионами мира и Европы.</p>

              <h3>  Видение</h3>
              <p>  Один из ведущих спортивных университетов и физического образования, реабилитации, оздоровления и отдыха в Европе.</p>
              <h3>  Миссия</h3>
              <p>  Университет работает для создания и предоставления последних исследований и технологии, основанные на всех уровнях непрерывного образования, устойчивого развития страны, физического воспитания, реабилитации, здравоохранения и зон отдыха.</p>
                <h3>Принципы</h3>
