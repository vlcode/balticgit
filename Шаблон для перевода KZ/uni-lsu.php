               <h2 class="hvr-icon-down" data-toggle="collapse" data-target="#hide-me2">Проживание</h2>

               <div id="hide-me2" class="collapse">
              <p>   Университет предлагает студентам общежитие находящиеся в университетском городке, в 5 минутах от центра города. Месячная арендная плата составляет 65 евро с человека. Также существует депозитный взнос в размере 58 евро, который оплачивается по прибытия и возвращается при выселении. Студенты живут по двое в одной комнате, есть возможность жить одному, при условии доплаты 65 евро и наличии свободных комнат.</p>

