               <h2 class="hvr-icon-down" data-toggle="collapse" data-target="#hide-me3">Преимущества обучения</h2>
               <div id="hide-me3" class="collapse">
              <p>   Университет на регулярной основе организует научные семинары, симпозиумы и научные конференции. Исследователи, преподаватели и студенты активно участвуют в национальных и международных научных конференций. Это дает студентам вузов и научным работникам работать на научной основе и обеспечивает стабильное академическое повышение квалификации.</p>

              <p>   Исследователи из Университета известны за высокие достижения в спортивной науке и исследования не только в Литве, но и за рубежом. Их статьи публикуются в престижных научных журналах, они читаются и оцениваются элитой в мире спортивной науки.</p>

              <p>   В университете фунционирует центр карьеры, главная цель которого помочь студентам и выпускникам перейти от академической жизни к профессиональному развитию и интеграции на рынке труда. Центр устанавливает и поддерживает тесные связи между работодателями и сообществом университета. </p>
              <p>   Организуются информационные методические семинары и лекции по карьерной занятости для студентов; </p>
              <p>   Организуются ярмарки, конференции и мероприятия по вопросам карьеры и занятости.</p>

              <p>   Библиотека LSU содержит более чем 121 000 публикаций, имеются залы для посетителей, читальные залы для групповой работы и индивидуального исследования с 90 рабочими местами, а также благоприятными условиями для самостоятельного изучения. Студенты имеют удаленный доступ к базам данных библиотеки из общежития и других помещений LSU.</p>

              <p>   LSU имеет академические соглашения об обмене с более чем 70 европейскими университетами и проводит активное членство в 14 международных организациях и сетях. Ежегодно университет отправляет и получает все большее число студентов и преподавателей через в рамках таких учебных программ мобильности как Erasmus и Nordplus.</p>

              <p>   <strong>ERASMUS+</strong> - Программа мобильности предусматривает обучение студентов в высших учебных заведениях других стран длительностью от 3 месяцев до одного года. Цель этой программы – совершенствовать образование студентов и развивать опыт обучения за рубежом.</p>
