               <h2>Об университете</h2>
               <p>Vilniaus Kooperacijos Kolegija - одна из самых старых школ по экономике в Литве. Его предшественником была Каунасская Школа Экономики, основанная в Каунасе в 1930 году. Вовремя немецко оккупации школа была закрыта, но после войны она снова возобновила свою работу.</p>

          <p> В 1956 году согласно решению Союза Литовских Кооперативов школа была перенесена в Вильнюс. С этого года школа выпустила большое количество высококвалифицированных специалистов в разных областях: бухгалтеры, экономисты, кондитеры, администраторы и т.д.</p>

        <p>   В 1999 году Министерство образования Литвы совместно с Центром оценки качества высшего образования признало VKK как одно из лучших не государственных высших учебных заведений.</p>

        <p>   Под существующим именем школа была основана 1 сентября 2000 года, на основе Вильнюсской вышей кооперативной школы.</p>

        <p>   Исследования и обучение - Мы помогаем каждому реализовать свои мечты, создавать и развивать свою уникальную карьеру и деятельность, с учетом их опыта и квалификации.</p>

