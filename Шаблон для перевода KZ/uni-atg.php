               <h2>Об университете</h2>
               <p>Организация была основана в 2007 году. Основной целью является обучение квалифицированных пилотов гражданской авиации. Офис находится на аэропорте "Spilve", где и проходят практические и теоретические занятия. Air Training занимает лидирующую позицию в сфере начального лётного образования в Латвии. Air Training обеспечивает высококачественную, легкую к восприятию, теоретическую подготовку, основанную на обширной базе обучающих материалов и опыте преподавательского состава.</p>
