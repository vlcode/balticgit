               <h2 class="hvr-icon-down" data-toggle="collapse" data-target="#hide-me3">Преимущества обучения</h2>
               <div id="hide-me3" class="collapse">

              <p>   Ощутите разницу: учить в школе предметы или учиться в школе профессии. В наше время человек, обладающий не только знаниями, но и реальными навыками, может рассчитывать на работу. Профессиональная средняя школа Victoria, созданная при Рижской Международной высшей школе экономики и управления (RSEBAA), готовит к жизни конкурентоспособную молодежь. Здесь за время обучения школьники к тому же успевают войти во вкус высшего образования, так как находятся в тесном контакте со студентами вуза.</p>

                 <p>Сегодняшние ученики заканчивают школу в 18, а то и 19 лет. Многим кажется совершенно неразумно сидеть за партой до столь почтенного возраста и в итоге, получить лишь корочку о среднем образовании. Профессиональная школа Victoria предлагает привлекательную альтернативу: всего один дополнительный год и у вас в кармане – еще и диплом конкурентоспособной на рынке труда профессии.</p>

              <p>   Известная в Латвии и за рубжом рижская международная высшая школа экономики и бизнеса RSEBAA предлагает получить не только высшее образование, но и среднее профессиональное. В стенах престижного вуза обучают школьников по трем профессиональным программам: гостиничный сервис, специалист по обслуживанию клиентов и дизайн интерьера.</p>

              <h3>   5 Актуальных вопросов о профессиональной школе «Victoria»</h3>

                <h3> 1. Для кого предназначено образование?</h3>

              <p>   Для учеников любых школ, которые в этом году закончили основную девятилетнюю школу. В первую очередь, эта прекрасная альтернатива средней школе, потому что уже с первого учебного дня ребята изучают профессиональные предметы. Программа выстроена таким образом, что каждый год обучения заканчивается практикой и получением сертификата изучаемой в данный год специальности, по окончании практики они имеют возможность работать, имея новые знания и навыки. во-вторых, если у ученика все-таки будет желание продолжить обучение в Высшей школе, наша профессиональная программа станет отличной подготовительной базой (для дальнейшего образования в RSEBAA – льготные условия поступления).</p>

              <h3>   2. Что дает обучение в школе Victoria?</h3>

                <p> Возможность одновременно получить среднее образование плюс освоить профессию. В программе «Гостиничного сервиса» – туризм, психология, кулинария, делопроизводство, менеджмент и т.д. На «Специалист по обслуживанию клиентов» изучают – делопроизводство, бухгалтерский учет, администрирование проектов, коммерческое право, психологию и и.д. На «Дизайне интерьера» – архитектуру, историю культуры, этику и этикет, и конечно предметы напрямую связанные с дизайном: композиция, рисование, проектирование, освещение, специальный компьютерные программы и т.д.</p>

              <h3>   3. Есть ли возможность практики, которая так необходима обучаясь, например, по программе «Гостиничный сервис»?</h3>

                <p> Ученики абсолютно всех программ имеют возможность пройти практику (как профессиональную, так и языковую) в Европейских странах (Англия, Франция). В вузах, а также компаниях, с которыми уже давно сотрудничает RSEBAA. Помимо практик Наши ученики могут принимать участие в различных проектах. В каждой программе проходит изучение 2-3 иностранных языков. В результате выпускники готовы для поступления на работу и в вузы за рубежом. На каждом курсе ребята проходят практику в той стране, язык которой они изучают.</p>

              <h3>   4. Чем отличается процесс обучения в VICTORIA от обычной средней школы?</h3>

              <p>   Мы предлагаем индивидуальный подход. Преподаватели школы VICTORIA – это не только профессионалы RSEBAA, но и лучшие преподаватели из других школ города, они помогут раскрыть различные таланты учеников и развить творческие способности. И очень важный фактор – преподаватели профессиональных предметов – «мастера» своего дела, авторы учебников, методик, пособий, профессионалы, любящие свое дело и готовые передать свои умения .</p>

              <h3>   5. На каком языке и где проходит обучение?</h3>

              <p>   Обучение в школе VICTORIA билингвально – на двух языках. Преподаватели читают лекции как на латышском, так и на русском языке. И для каждого школьника это дополнительное развитие. Обучение проходит в здании высшей школы RSEBAA. Ученики нашей школы также пользуются библиотекой, компьютерном залом, и тд.</p>
