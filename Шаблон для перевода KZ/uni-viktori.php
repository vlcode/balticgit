               <h2>Об университете</h2>
              <p>  Профессиональная средняя школа Victoria (на базе) Рижской международной высшей школы экономики и управления RISEBA) дает возможность получить среднееобразование и специальность, которая востребована на рынке труда.</p>

              <h3>  1. Школа внутри вуза</h3>

              <p>  Профессиональная средняя школа Victoria работает под крышей Рижской международной высшей школы экономики и управления RISEBA, поэтому ее учащиеся тесно общаются со студентами и преподавателями, а также участвуют в различных программах RISEBA.Старшеклассники вращаются в студенческой среде, где мысль о высшем образовании, о карьере и вообще о будущем просто витает в воздухе! Понятно, что отношение преподавателей к детям — как к взрослым.В школе Victoria работает абсолютно звездный преподавательский состав. Плюс база высшей школы, прекрасное современное оснащение и материалы.</p>

              <h3>  2. Бесценная производственная практика + профессия</h3>

              <p>  Мы считаем, что ребятам к моменту получения диплома о высшем образовании не менее важно иметь специальность, которая к тому же востребована на рынке труда, — считает Елена Поминова. — Выпускники девятых классов имеют возможность получать у нас не только среднее, но и профессиональное образование сразу по трем направлениям: гостиничный бизнес, дизайн интерьера, административная работа.</p>

              <p>  Каждая программа рассчитана на четыре года обучения: первые три года — параллельно с общеобразовательными дисциплинами, последний год идет углубленное проникновение в профессию — и еще обязательные квалификационные практики. Старшеклассники получают опыт общения с потенциальными работодателями, познают нюансы выбранной ими специальности.</p>

              <p>  Учащиеся, которые осваивают гостиничный бизнес, проходили практику в 4-звездочных отелях Латвии, в том числе в Риге и Юрмале. Ребята приобрели просто бесценный опыт: лето, как известно, выдалось горячим — в прямом и переносном смысле.</p>

              <p>  Будущие дизайнеры из школы Victoria получили заказ от конкретных предпринимателей — занимаются проект-предложением по декорированию кафе на улице Цитаделес и оформлением кафе на улице Алунана в Риге.Кстати, помимо частных компаний учащиеся школы Vicroria проходят практику и в RISEBA, в том числе в существующем при вузе Международном культурном центре, с которым у школы сложилось тесное сотрудничество. Дело в том, что этот центр организует очень много разных мероприятий — концерты, творческие и культурные встречи с приглашением послов разных стран. К этой деятельности привлекают старшеклассников школы Victoria. Школьники принимают участие в оформлении мероприятий, встрече гостей и так далее.</p>
              <h3>  3. Изучение языков</h3>

              <p>  В школе действует реальное билингвальное обучение. Плюс интенсивное изучение английского и французского. Стоимость обучения в школе — 160 евро в месяц.</p>
