                   <p>   В 1921 году в здании училища по ул. Владимирской, 32 была открыта педагогическая школа. В 1922 году она переименована в педагогический семинар. Далее с 1923 года Даугавпилсский государственный учительский институт, с 1952 года Даугавпилсский педагогический институт, в 1986 году институту присвоено имя Яна Калнберзина, с 1993 года — Даугавпилсский педагогический университет, с 2001 года — Даугавпилсский университет. </p>

            <p>  Даугавпилсский университет на сегодняшний день является крупнейшим региональным государственным университетом и единственным университетом в Восточной Латвии. DU предлагает получить знания на 5 факультетах и 53 учебных программ. Основными научными дисциплинами DU являются: литературоведение, языкознание, история, биология, наука об окружающей среде, физика, экономика, социология, психология, искусство.</p>

            <p>  4 ноября 2011 года прошли мероприятия празднования 90-летия со дня основания Университета в самом университете и в городском театре.</p>
            <p>  Университет сотрудничает примерно с 80 учреждениями по всему миру, и недавно стал членом Европейской ассоциации университетов. Даугавпилсский университет участвует в международной программе Erasmus. </p>

