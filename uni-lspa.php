                   <p><strong>Латвийская академия спортивной</strong> педагогики является единственным вузом среди специализированных высших учебных заведений Латвии, который готовит специалистов по спорту. </p>

                   <p>Академия была образована 6 сентября 1921 года как Латвийский институт физического образования, а 8 ноября 1991 года он был переименован в Латвийскую академию спортивной педагогики (ЛАСП). ЛАСП является аккредитованным (14.07.1998) государственным высшим учебным заведением, в котором обучение проводится на латышском и английском языках. Постановление о создании Спортивной академии было утверждено кабинетом министров Латвийской республики под номером 352.</p>

                   <p>Латвийская академия спортивной педагогики (ЛАСП) является государственным высшим учебным заведением, которое осуществляет аккредитованные программы обучения, чтобы подготовить дипломированных специалистов для работы в учреждениях и организациях сферы образования, спорта, а также в государственных и муниципальных организациях и предпринимательских обществах. Студенты получают знания, умения и навыки, необходимые для того, чтобы руководить и организовывать деятельность в области образования, спорта, укрепления здоровья и других видов деятельности, а также следующие квалификации: учитель спорта (физкультуры), старший тренер по виду спорта, руководитель в области спорта, специалист по рекреации, физиотерапевт. </p>

                   <p>На протяжении 90 лет существования Латвийская академия спортивной педагогики подготовила более 10 000 специалистов в области спорта.</p>
