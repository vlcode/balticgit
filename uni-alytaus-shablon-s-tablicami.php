              <p>   Миссия университета, обучение квалифицированных специалистов в области бизнеса и технических направлений, способных конкурировать на международном и местном рынке труда. Развить в студентах творческое и креативное мышления, сформировать социальные, культурные и моральные ценности.</p>

              <p>   В университете открыто 3 факультета, на которых студенты получают и развивают свои теоретические, практические знания и навыки.</p>

