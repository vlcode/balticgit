                <p> Для студентов создана эффективная система образования. Лекции проходят в современных классах, студенты могут воспользоваться богатым архивом Центра информации. Студентам открыт доступ к материалам видеотеки, аудиотеки, которые находятся в современной библиотеке с доступом к интернету и обширной научной базе данных.</p>
