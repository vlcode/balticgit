            <p>  Сырттай оқу нысаны – өз бойында өздігінен оқу мен күндізгі оқуды ұштастырады. Пәндердің басым бөлігін студенттер өз бетімен меңгереді. Сырттай оқу нысанының негізгі өзгешелігі оның ұзақтығы және жұмыс қызметін жалғастыру мүмкіндігі болып табылады. Сырттай оқу нысанының студенттері екі кезең оқиды. Алғашқы кезең – кіріспе лекциялар, оларда оқытушылар әдебиет тізімін, рефераттардың тақырыптарын, бақылау жұмыстарын және т.б. береді. Екінші кезең – емтихан сессиялары.</p>
