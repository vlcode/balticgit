                    <p>  Балтық елдерінде орыс, ағылшын, неміс, белорусс, украин тілдерінде және басқа тілдерде оқуға мүмкіндік бар.</p>
                    <p>  Балтық елдерінің жоғарғы оқу орындары деңгейі мен ұзақтығы әр түрлі оқуды ұсынады.</p>
