  <li>  • Бағдарламаның тәптіштелген мазмұны – “Литваны (Балтық аймағын) зерттеу” шәкіртақысы үшін</li>
  <li>  • Ағылшын тілін меңгеру туралы сертификаттың көшірмесі (IELTS, TOEFL, CPE), егер сіз Балтық орталығыда емтихан тапсырған болсаңыз, сізге растайтын хат беріледі.</li>
