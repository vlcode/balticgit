                   <p>  Оглядываясь на историю Латвийского Университета (ЛУ), мы окажемся в более раннем времени, чем дата основания 28 сентября 1919 года. Его создание не случайно, оно вызвано внешними историческими обстоятельствами, и как результат латвийской интеллектуальной зрелости, накопленной за многие десятилетия. Идея о создании высшего учебного заведения на национальном уровне была выдвинута во второй половине XIX века социальными и культурными деятелями, призывающими народ Латвии стремиться к знаниям и качественному образованию. Их призывы были услышаны и выполнены в начале XX века.</p>

            <p>  Первый съезд латвийских преподавателей состоялся 7-13 июня 1917 года в Тарту, Эстонии, и имел решающую роль в создании Национального Высшего Учебного Заведения во главе с философом Паулс Дейл в качестве первого председателя. 13 июня 1917 года было издано постановление «О латвийской высшей школе", которое содержало одно из определяющих принципов Нового Университета - единство гуманитарных, естественных и технических наук. В формирование и основании Латвийского университета участвовал комитет, который состоял из П. Дейл, Э. Фельсбергс, Д. Осис, П. Номалс, Э. Лаубе, А. Бутулс, К. Каспарсонс и многих других уважаемых интеллектуалов. Некоторые из них были в числе первых преподавателей факультета Латвийского университета.</p>

            <p>  Торжественное открытие первой в Латвии Высшей Школы состоялось 28 сентября 1919 в здании бывшего Рижского Политехнического Института на бульваре Райна 19 С принятием Конституции было указано переименовать Высшую Школу в Латвийский Университет. Новое высшее учебное заведение стало одним из важнейших манифестов свободы латышского народа. Поэтому Латвийский Университет более тесно связан с новой историей Латвии, чем любой другой ВУЗ.</p>

            <p>  Последующие годы в истории Латвийского университета были отмечены особенно интенсивным развитием - принятием Конституции, принятием и прохождением бюджета в парламенте, рекрутингом выдающихся преподавателей, расширением учебных программ, переработкой латвийской научной терминологии и созданием академических традиций. Преподаватели и студенты нашли общие цели, что позволило им подняться над рутиной и мирскими проблемами. С 1919 по 1940 г. университет был крупнейшим центром высшего образования, науки и культуры в Латвии, где формировалась интеллигенция. В Латвийском государстве высоко ценилось образование и поддерживалось и морально, и материально. Например, на образование было выделено 14,7% от общей суммы бюджетных расходов в учебном году 1937/38 (в остальной Европе - 12,1%). В государственных расходах, образование заняло третье местное превзойдя только продвижение экономической жизни и меры обороны.</p>

            <p>  Латвийский Университет призывал к научно-исследовательским работам, способствовал распространению науки среди людей и подготавливал высококвалифицированных специалистов в соответствии с национальными потребностями. Со временем новые преподаватели и поколения ученых выросли и UL стал частью научной жизни. В 1939 году было 13 900 людей с высшим образованием в Латвии, 59% из них UL выпускников, примерно 30,1 студентов на каждые 10 000 человек (в Швеции - 14,2).</p>

            <p>  Латвийский университет сохранил свою главную цель - быть в центре латвийской интеллектуальной жизни путем формулирования новых задач и целей. В настоящее время созданы новые факультеты и учебные программы с целью повышения качества исследований, в научно-исследовательской деятельности. За 90 лет своего существования, Латвийский университет превратился в один из ведущих высших учебных заведений, около 22 тысяч студентов и более 1400 преподавателей. Развитие Латвийского университета представляет собой непрерывный процесс на основе богатого и обширного исторического наследия!</p>

              <h3>Медицинской факультет</h3>
            <p>  Медицинский факультет (МФ) Университета Латвии был создан 28 сентября 1919 года, для подготовки врачей, но был отменен в советский период, затем восстановлен 29 сентября 1997 года, а торжественное открытие состоялось 4 сентября 1998 года. В течение учебного 2008/2009 года на МФ учились 851 студент, включая студентов из-за рубежа. Стабилен рост числа студентов факультета каждый год, и в настоящее время Минфин Университета Латвии регистрирует около 200 новых студентов каждый год.</p>

            <p>  На Факультете работает в целом 122 члена профессорско-преподавательского состава, 3 из которых являются полноправными членами Латвийской академии наук, 7 корреспондентов, 13 профессоров, 18 доцентов, 21 доцент, 6 профессоров гостей, 5 отдельных доцентов и т.д.</p>

            <p>  Минфин Университета Латвии предлагает следующие аккредитованные учебные программы: Высшую профессиональную программу исследования в области физики, наук о здоровье, бакалавриат и магистратуру по программам в области медсестринского дела, бакалавров и магистров по программе «фармацевтическое дело» и несколько программ для обучения в аспирантуре. Все программы являются современными, соответствующими последним научным достижениям и опыту скандинавских стран. Процесс исследования является демократическим и открытым и основан на принципе - студент является центром исследований. Помимо обязательных учебных курсов, Минфин предлагает Университету Латвии, широкие возможности которые напрямую не относятся к избранной специальности, но будут полезны в дальнейшей профессиональной деятельности, например, иностранные языки, психология, экономика и вопросы, связанные с законами.</p>

            <p>  Неотъемлемой и важной частью учебного процесса является практика, начиная с 3-го года обучения. Практика для студентов МФ Университета Латвии проходит в ведущих клиниках и медицинских учреждениях Риги. Существует также широкое сотрудничество в области научных работ с несколькими научными учреждениями Латвии, что дает возможность для укрепления академических знаний и мотивирует сосредоточиться на развитии науки и принятии участия в научно-исследовательских проектах.</p>
