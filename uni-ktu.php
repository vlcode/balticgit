                 <p> История Каунасского университета начинается с 27 января 1920 года. Тогда математик Z. Žemaitis и психолог Дж. Вэбэлас-Гудэйтис вместе с другими ученными и профессорами, основали первый центр учебный центр в Каунасе, где стало обучаться 480 человек и работало 48 учителей. С течением времени школа разрасталась, открывались новые направления и к нашему времени Каунасский университет является одним из крупнейших технических вузов в Балтии.</p>

                 <p>  KTU является одним из лидеров научных исследований в области физики и технологий. Результаты научных исследований, проводимых в KTU, помогают создавать революционные решения научных и практических проблем: от проектирования умных приборов, помогающих инвалидам, до создания крайне точных инструментов для медицинской диагностики и осуществления инноваций в космическом и ядерном оборудовании.</p>

