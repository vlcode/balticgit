                <p><b>В чем различие между стипендией, грантом и скидкой на обучение за рубежом</b></p>
                
                <p><b>Грант</b> - это безвозмездная субсидия от предприятия, государства или университета на обучение. Существует большое количество университетов и организаций предоставляющих гранты для зарубежных студентов, которые частично или полностью покрывают расходы на обучение и проживание. Гранты выдаются на конкурсной основе и для его получения вам необходимо показать комиссии, что вы являетесь достойным кандидатом и предоставить свои планы на исследовательскую, общественную или другую значимую деятельность.</p>
                
                <p><b>Стипендия</b> - деньги, выплачиваемые вузом, государством или фондом по результатам вашего обучения. Стипендия также может частично или полностью покрывать ваши расходы на обучение. Основанием для выдачи стипендии являются ваши успехи в обучении, общественная деятельность и спортивные достижения.</p>
                
                <p><b>Скидка</b> - сумма на которую снижается стоимость обучения. Скидки на обучение выдаются непосредственно
                      университетом и в некоторых случаях организациями или фондами. Сумма скидки устанавливается университетом или организацией и может варьироваться от 5% до 50%. Так же как и в случае со стипендиями, основанием для получения скидки являются ваши успехи в учебе, общественная деятельность и спортивные достижения.</p>

