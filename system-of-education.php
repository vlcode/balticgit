                            <b>Заочная форма обучения</b> – сочетает в себе самообучение и очное обучение. Большую часть предметов студенты осваивают самостоятельно. Основным отличием заочной формы обучение является ее продолжительность и возможность продолжения рабочей деятельности. Студенты заочной формы обучения учатся в два периода. Первый период – это вводные лекции, на которых преподаватели дают список литературы, темы рефератов, контрольные задания и т.д. Второй период – это экзаменационные сессии.
