                 <div class="tab-pane fade" id="living">
                   <p>С 1 октября начинается заселение студентов ЕГУ в одно из студенческих общежитий «VU Būstas» (аллея Саулетекё (Saulėtekio al.) дд. 31-51), которые находятся в районе Антакальнис. Общежития расположены рядом со зданием учебного корпуса ЕГУ – до него можно добраться пешком либо проехать две остановки на троллейбусе/автобусе. Студенты размещаются также в общежитиях в другом студенческом городке на улице Дидлаукё (Didlaukio g.).</p>

                <h3>Проживание в общежитии</h3>
                <p>Студенты размещаются в 3-местных комнатах; все комнаты с балконом, большинство – с санузлом в комнате. На каждом этаже есть общая кухня; в комнатах имеется доступ к Интернету.</p>

                <p><b>Внимание:</b> курение в студенческом общежитии запрещено!</p>

                <p>Стоимость одного месяца проживания в общежитии составляет 70 евро (включая коммунальные услуги). Студенты заключают договор аренды на 12 месяцев (с 1 октября по 30 сентября).</p>

                 </div>

                 <!-- Блок "Преимущества обучения" -->
                 <div class="tab-pane fade" id="advantages">
                   <p>ЕГУ предоставляет своим студентам отличные возможности участия в международных программах студенческого обмена, например, Erasmus+ или Campus Europae. Учебные кредиты, полученные в университете-партнере, впоследствии засчитываются в ЕГУ. Таким образом, каждый студент может в течение 1–2 семестров обучаться в одном из западноевропейских университетов-партнеров.</p>

                 <p>Студентам предоставляется широкий ряд возможностей для проведения исследований в рамках деятельности научно-исследовательских центров и лабораторий. Студенты ЕГУ принимают участие в мастер-классах, летних школах, конференциях, конкурсах научных студенческих работ. Основой учебы в университете являются академические свободы: студенты ЕГУ не подвергаются дискриминации по причине языка, мнения, объекта исследования. В ЕГУ у каждого студента и преподавателя имеется право на свободу мысли и слова.</p>

                 <p>Для студентов ЕГУ создана возможность получать знания от белорусских, западноевропейских и американских преподавателей, дискутировать и перенимать их научный и культурный опыт.</p>

                 <p>Относительно небольшое количество студентов в группе позволяет более эффективно усваивать материал, обсуждать свои идеи и вопросы с преподавательским составом. На 1 преподавателя приходится 16 студентов, что позволяет выстроить взаимодоверительные отношения между всеми участниками учебного процесса.</p>

                 <p>В основном преподавание ведется на русском языке, однако присутствует достаточное количество курсов на белорусском, английском и других языках. Обязательным является изучение 2 иностранных языков в течение первых двух лет обучения. Существует возможность продолжить изучение иностранных языков во время дальнейшего обучения. Также в течение первого года обучения студенты изучают курс литовского языка и культуры. Стоит отметить, что обучение в рамках предоставляющей двойной диплом бакалаврской программы «Мировая политика и экономика» осуществляется исключительно на английском языке.</p>

                 <p>Расписание занятий в ЕГУ варьируется в зависимости от графика посещения университета преподавателями-визитерами из Беларуси и других стран, что позволяет студентам привыкнуть к гибкому учебному графику.</p>
