                   <p>   Вильнюсский колледж дизайна (VCD) - это частное высшее учебное заведение, основанное для того, чтобы предложить высококачественное образование в области искусств для творческих и креативных людей.</p>

            <p>   В 1997 году художник и модельер, Giedrė Fledžinskienė, с 30-летним педагогическим и творческим опытом основал Giedre Fledžinskienė Высшую школу искусств, готовящую модельеров и театральных make-up специалистов. В 2005 году школа была награждена официальным статусом колледжа и была переименована в Вильнюсский колледж дизайна.</p>

            <p>   2 июля 2009 года Литовский центр оценки качества высшего образования и Совета по оценке качества в области науки и учебных заведений завершили оценку Вильнюсского колледжа дизайна и дали положительную оценку его деятельности. Колледж получил разрешение на присуждение профессиональной степени бакалавра.</p>

            <p>   Сообщество. В настоящее время Вильнюсский колледж дизайна состоит из 500 студентов и 60 сотрудников.</p>

          <p>     Среди наиболее перспективных профессий в эти дни в Литве - дизайн интерьера, дизайн одежды и прикладной фотографии. VCD является отличным местом для художественных и творческих людей желающих учиться, чтобы улучшить свои навыки и получить степень бакалавра в динамичной и дружественной обстановке учреждения высшего профессионального образования.</p>

          <p>     Творчески структурированных обучающих программ и профессионально квалифицированные преподаватели колледжа применяют прогрессивные методы обучения. Администрация совместно с профессорско-преподавательским составом создает не только идеальные условия для работы и учебы, но и дружественную атмосферу в колледже. Работы студентов и выпускников свидетельствуют о высоком профессиональном уровне обучающего персонала. Благодаря опыту и знаниям, полученным в течение многих лет практики. и использование современных технологий, обучение в Вильнюсском колледже дизайна обеспечивает отличный старт в мире профессионального дизайна.</p>
