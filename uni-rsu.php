                   <p>RSU является государственным университетом, предлагающий различные учебные программы и обеспечивает реализацию научных проектов, давая превосходное образование и подготовку специалистам в области здравоохранения и социальных наук, способных работать в Латвии, Европейском союзе и по всему миру.</p>

               <p>История вуза начинается после второй мировой войны. В 1950 году на базе медицинского факультета Латвийского Государственного Университета был создан Рижский Медицинский Институт. Инициаторами были Паулс Страдынс, Эрнест Буртниеку (первый директор Рижского Медицинсокго Института) и министр здравоохранения Адольф Краусс. </p>

               <p>Во времена СССР Рижский Медицинский Институт считался лучшим учебным заведением в области здравоохранения и готовил высококвалифицированных докторов.</p>

               <p>В 1990 году институт был переименован в Латвийскую Медицинскую Академию, но 5 апреля 1998 года, согласно решению учредительского собрания академия была переименована в Рижский Университет Страдыня (RSU), который со временем превратился в вуз университетского типа, так как там стали готовить специалистов не только в области медицины и фармацевтики, но и в области социальных наук, естественных наук, здравоохранения и права.</p>
