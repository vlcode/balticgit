                   <p>Твоим будущим местом работы могут стать европейские переводческие бюро, международные организации, частные и государственные предприятия.</p>

                   <p>Академический персонал, осуществляющий программу «Перевод с/на английский – русский – немецкий/французский», состоит из профессиональных педагогов, переводчиков и специалистов в разных сферах, международных экспертов, председателей научных советов и комитетов, членов Европейского общества ученых-переводчиков. Практические занятия по письменному и устному переводу ведутся на русском языке, но большой пласт лекций преподается и на английском, что позволяет студентам получать разносторонние теоретические знаниями и овладевать терминологией в разных научных, экономических и социально-политических областях.</p>

