                 <div class="tab-pane fade" id="living">
                   <p> Вентспилсская высшая школа обоснованно гордится одной из лучших студенческих служебных гостиниц в Латвии. Она располагается в одном здании с вузом, поэтому ни снег, ни дождь, ни сильный ветер не мешают студентам получать знания, ведь на лекции можно попасть, просто пройдя через стеклянный переход, объединяющий корпусы с гостиницей и аудиториями.</p>

                   <p>Служебная гостиница размещается в двух корпусах вуза, на шести и восьми этажах, в просторных, светлых, благоустроенных помещениях. Студенты живут по двое в комнате. За 12 латов в год можно подключиться к компьютерной сети и пользоваться услугами коммерческого провайдера, либо бесплатно использовать подключение к Интернету, обеспечиваемое вузом.</p>

                   <p> Подобно другим хорошим общежитиям, у каждой комнаты имеется свой душ, так что стоять в длинных очередях не придется. На всех этажах оборудовано по несколько кухонь, где студенты могут готовить себе обеды, а если кашеварить лень, можно подняться на 6-й этаж в кафетерий Панорама, откуда открывается прекрасный вид на море.</p>

                   <p> Общежитие состоит из блоков, в каждом из них две комнаты, душ и туалет. Все комнаты меблированы – там стоят шкаф, книжная полка, стулья, стол и кровать. Во всех комнатах доступен беспроводной Интернет, а также имеется возможность подключиться к сети. На каждые пять блоков оборудована одна кухня, а на шестом этаже служебной гостиницы работает кафетерий.</p>

