                 <div class="tab-pane fade in active" id="about-uni">
                   <p> Вильнюсская академия искусств - является крупнейшим и самым старым художественным университетом в Балтийском регионе. Основной миссией академии является улучшение качества подготовки специалистов в области искусства.</p>

            <p>  Академия была основана в 1793 году, как отдел Архитектуры в Главной школе Великого Герцогства Литвы в Вильнюсе. В конце 19 века, данный отдел был закрыт царским правительством вместе с Вильнюсским университетом, и затем вновь открыт в 1919 году. После 2 мировой войны академия была объединена с Каунасским Институтом Искусств и была переименована в Институт Искусств Литовской Советской Социалистической Республики. В 1990 году академии вернули прежнее название Вильнюсская Академия Искусств.</p>

          <p>    С 1990 года почти 100 выпускников Академии были награждены национальными премиями в области искусств. На сегодняшний день в академии обучается более 1800 студентов, в четырех городах - Вильнюс, Каунас, Клайпеда и Telšiai.</p>

            <p>  Отделение академии в Вильнюсе предлагает обучение в области художественного искусства, программы варьируются от живописи до менеджмента в области культуры. В Каунасе предлагают учебные программы по направлению стеклянного искусства, они являются уникальными во всем Балтийском регионе. В Telšiai учебные программы связанны с работой над твердыми материалами (метал), а в Клайпеде программы связанные с актуальными художественными тенденциями.</p>
